<p>To authenticate requests, include a <strong><code>X-XSRF-TOKEN</code></strong> header with the value <strong><code>"{X-XSRF-TOKEN}"</code></strong>.</p>
<p>All authenticated endpoints are marked with a <code>requires authentication</code> badge in the documentation below.</p>
<p>All requests to this API must include the header X-XSRF-TOKEN.</p>
<p>The value for this header is obtained by calling the route <code>/sanctum/csrf-cookie</code> once.</p>
<p>This endpoint does not send any response data but will automatically set two cookies in your browser:</p>
<ul>
<li>laravel_session</li>
<li>XSRF-TOKEN</li>
</ul>
<p>Use the second cookie value as value for the header on subsequent requests.</p>
<p>If any of the API endpoints returns a 419 (token mismatch) error, you might have to request a new one. If the error persists or the cookie is not set, please contact any of the backend developrs.</p>
